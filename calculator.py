def add(a, b):
    """Add two numbers and return the result."""
    return a + b

def subtract(a, b):
    """Subtract b from a and return the result."""
    return a - b

def multiply(a, b):

    return a * b

def div(a,b):

    return a / b

# Test our functions
print(f"5 + 3 = {add(5, 3)}")
print(f"10 - 4 = {subtract(10, 4)}")
print(f'2 * 5 = {multiply(2,5)}')
print(f'10 / 2 = {div(10,2)}')